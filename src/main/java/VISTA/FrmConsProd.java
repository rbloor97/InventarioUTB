    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        // TODO add your handling code here:
        
        if(tf_codigo.getText() != null && !"".equals(tf_codigo.getText())){
            limpiarTabla();
            ArrayList<Producto> arrProd = ivt.mostrar(tf_codigo.getText());
            for (Producto p: arrProd){
                String rowData[] = { p.getId_producto(), p.getNombre(), p.getCantidad(), p.getPrecio(),p.getEstado(),p.getDescripcion() };
                model.addRow(rowData);
            }
            tb_datos.setModel(model);
            
        }
        
    }//GEN-LAST:event_btn_buscarActionPerformed

