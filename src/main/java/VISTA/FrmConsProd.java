    private void btn_consultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_consultarActionPerformed
        // TODO add your handling code here:
        limpiarTabla();
        ArrayList<Producto> arrProd = ivt.mostrarTodos();
            for (Producto p: arrProd){
                String rowData[] = { p.getId_producto(), p.getNombre(), p.getCantidad(), p.getPrecio(),p.getEstado(),p.getDescripcion() };
                model.addRow(rowData);
            }
            tb_datos.setModel(model);
    }//GEN-LAST:event_btn_consultarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        int fila = tb_datos.getSelectedRow();
        if(fila != -1){
            if(ivt.eliminar(model.getValueAt(fila, 0).toString())){
                limpiarTabla();
                ArrayList<Producto> arrProd = ivt.mostrarTodos();
                for (Producto p: arrProd){
                    String rowData[] = { p.getId_producto(), p.getNombre(), p.getCantidad(), p.getPrecio(),p.getEstado(),p.getDescripcion() };
                    model.addRow(rowData);
                }
                tb_datos.setModel(model);
            }
            
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

